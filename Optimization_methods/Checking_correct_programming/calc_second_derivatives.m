h = 0.000001;
x1 = 3; x2 = -2;
[f,g,H] = feval(@gradgess, [x1 x2]);
f1 = feval(@gradgess, [x1+2*h x2]);
f2 = feval(@gradgess, [x1+h x2+h]);
f3 = feval(@gradgess, [x1 x2+2*h]);
f4 = feval(@gradgess, [x1+h x2]);
f5 = feval(@gradgess, [x1 x2+h]);
H1 = (f1-2*f4+f)/(h*h);
H2 = (f2-f4-f5+f)/(h*h);
H3 = (f3-2*f5+f)/(h*h);
disp(strcat('f_x1x1(x1,x2)=',num2str(H(1,1))));
disp(strcat('df_x1x1(x1,x2)=',num2str(H1)));
disp(strcat('Абсолютная погрешность=',num2str(abs(H1-H(1,1)))));
disp(strcat('Относительная погрешность=',num2str(abs((H1-H(1,1))/H1))));
disp(strcat('f_x1x2(x1,x2)=',num2str(H(1,2))));
disp(strcat('df_x1x2(x1,x2)=',num2str(H2)));
disp(strcat('Абсолютная погрешность=',num2str(abs(H2-H(1,2)))));
disp(strcat('Относительная погрешность=',num2str(abs((H2-H(1,2))/H2))));
disp(strcat('f_x2x2(x1,x2)=',num2str(H(2,2))));
disp(strcat('df_x2x2(x1,x2)=',num2str(H3)));
disp(strcat('Абсолютная погрешность=',num2str(abs(H3-H(2,2)))));
disp(strcat('Относительная погрешность=',num2str(abs((H3-H(2,2))/H3))));
